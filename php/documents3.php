<hr /><center><a id="exercice1">Première partie</a><br /></center><hr /><ul>
<li><b>Initialisation GitHub</b>
<ol>
<li>Compte GitHub : Créer un compte sur GitHub.</li>
<li>Compte GitHub : Explorer les menus sur GitHub.</li>
<li>Installation de Git : Installer le package Git en local.</li>
<li>Confiuration : Configurer en local votre compte Git.</li>
</ol>
</li>
<li><b>Git les bases</b>
<ol start="5">
<li>Création Repo : créer un repository sur GitHub et le récupérer le en local.</li>
<li>Cet exercice est à réaliser à deux.<br />Création Repo : créer un repository en local et se connecter à un repository distant. Puis, votre voisin se connecte à votre repository.</li>
<li>Manipulation de fichiers : créer un fichier texte avec du contenu, ajouter ce fichier au repository distant. En parallèle, votre voisin doit récupérer les modifications effectuées.</li>
<li>Manipulation de fichiers : inverser les rôles pour les dexu exercices précédents.</li>
<li>Manipulation de fichiers : reprendre les exercices précédents mais en supprimant un fichier du repository distant.</li>
<li>Commits : afficher la liste des commits</li>
</ol>
</li>
</ul><center><a href="git/Serie_1.docx">Énoncé au format docx</a>.</center><br />
<hr /><center><a id="exercice2">Deuxième partie</a><br /></center><hr /><ul>
<li><b>Utilisation d’un repository existant</b></li>
<ol>
<li>Connexion à un repository : se connecter au repository public : <a href="https://github.com/CyrilMarcq/isn20232024" target="_cmgit">https://github.com/CyrilMarcq/isn20232024</a>.</li>
<li>Ajout d'un fichier : ajouter un fichier texte nommé par votre nom et contenant quelques informations.</li>
<li>Synchronisation : récupérer les modifications de tout le monde.</li>
</ol>
</li>

<li><b>Gestion d'un conflit</b></li>
<ol  start="4">
<li>Modification d'un fichier : modifier votre fichier et celui de votre voisin et envoyer les modifications sur le repository distant. En cas de conflit, les corriger.</li>
</ol>
</li>
<li><b>Les Branches</b>
<ol start="5">
<li>Création branche : créer une branche d'évolution avec votre nom d'origine la main et créer une branche de correction de bug avec votre nom d'origine la main.</li>
<li>Utilisation des branches : basculer d'une branche à l'autre.</li>
<li>Utilisation des branches : afficher la liste des branches existantes.</li>
<li>Modification des fichiers : modifier le même fichier sur les 2 branches mais avec du contenu différent et envoyer sur le repository distant.</li>
<li>Fusion des branches : fusionner les 2 branches en une : la correction de bug doit se retrouver dans l'évolution et corriger le conflit éventuel.</li>
<li>Commits : afficher la liste des commits sur la branche restante.</li>
</ol>
</li>
</ul><center><a href="git/Serie_2.docx">Énoncé au format docx</a>.</center><br />
